# Normaliza as chaves para facilitar a busca (Lower Case Map)
_LOWER_MAP = {k.lower(): v for k, v in CORPSE_IDS.items()}

def get_corpse_id(monster_name):
    """
    Retorna o ID do corpo do monstro.
    É insensível a maiúsculas/minúsculas (Case Insensitive).
    """
    if not monster_name: 
        return 0
        
    clean_name = monster_name.strip().lower()
    return _LOWER_MAP.get(clean_name, 0)