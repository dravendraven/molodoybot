# Funes auxiliares
def get_loot_ids():
    """Retorna lista de todos os IDs lootveis."""
    return list(LOOTABLES.keys())

def get_loot_info(item_id):
    """Retorna dict de info ou None."""
    return LOOTABLES.get(item_id, None)

def get_loot_name(item_id):
    """Retorna nome do item ou 'Unknown'."""
    data = LOOTABLES.get(item_id)
    return data['name'] if data else 'Unknown'

def get_loot_weight(item_id):
    """Retorna peso em oz ou 0.0."""
    data = LOOTABLES.get(item_id)
    return data['weight'] if data else 0.0

def find_loot_by_name(name_query):
    """
    Busca item por nome (case-insensitive, partial match).
    Retorna lista de IDs que batem com a query.

    Exemplos:
        'gold'  [3031] (gold coins)
        'plate'  [3357, 3386, ...] (plate armor, plate shield, etc)
        'sword'  [3264, 3283, ...] (sword, two handed sword, etc)
    """
    results = []
    query_lower = name_query.lower().strip()
    for item_id, data in LOOTABLES.items():
        if query_lower in data['name'].lower():
            results.append(item_id)
    return results

def is_stackable(item_id):
    """
    Verifica se o item  empilhvel (tem flag 'Cumulative').
    Retorna True se empilhvel, False caso contrrio.
    """
    data = LOOTABLES.get(item_id)
    if data and 'flags' in data:
        return 'Cumulative' in data['flags']
    return False
